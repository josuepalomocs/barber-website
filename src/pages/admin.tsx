import Head from "next/head";
import BottomNavigation from "@/components/BottomNavigation/BottomNavigation";
import useBottomNavigation from "@/components/BottomNavigation/useBottomNavigation";
import HomeScreen from "@/components/admin/Screens/HomeScreen";
import AnalyticsScreen from "@/components/admin/Screens/AnalyticsScreen";
import BookingsScreen from "@/components/admin/Screens/BookingsScreen";
import ShopScreen from "@/components/admin/Screens/ShopScreen";

export default function Home() {
  const { selectedScreen, setSelectedScreen } = useBottomNavigation();

  function renderHead() {
    return (
      <Head>
        <title>OCFADES Admin</title>
        <meta name="description" content="Generated by create next app" />
        <meta name="viewport" content="width=device-width, initial-scale=1" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
    );
  }

  function renderSelectedScreen() {
    if (selectedScreen === "home") return <HomeScreen />;

    if (selectedScreen === "analytics") return <AnalyticsScreen />;

    if (selectedScreen === "bookings") return <BookingsScreen />;

    if (selectedScreen === "shop") return <ShopScreen />;

    throw new Error("Failed to render unsupported screen.");
  }

  return (
    <>
      {renderHead()}
      {renderSelectedScreen()}
      <BottomNavigation
        selectedScreen={selectedScreen}
        setSelectedScreen={setSelectedScreen}
      />
    </>
  );
}
